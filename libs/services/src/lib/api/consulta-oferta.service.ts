@Injectable({
  providedIn: 'root',
})
export class ConsultaOfertaService {
  private iterationStatus = true;

  constructor(private http: HttpClient, private authService: AuthService) {}

  // OFFERT PAGE SERVICE
  getOfferByOfferId({ id }: { id: string | number }) {
    // return id of consulting
    return new Promise(async (resolve, reject) => {
      this.http
        .get<offerBaseT>(environment.apigateway_url + '/offers/' + id, {
          headers: {
            'Content-Type': 'application/json',
            'Cache-Control': 'no-store, no-cache, must-revalidate',
            
            Authorization:
              'Bearer ' + (await this.authService.currentAuthToken),
          },
        })
        .subscribe((value) => {
          resolve(formatOfferData(value));
        });
    });
  }

  initIterationInstance() {
    this.iterationStatus = true;
  }
  destroyIterationInstance() {
    this.iterationStatus = false;
  }
  asyncTimeOutFn($this: asyncCallParamsT) {
    $this.service().then(async (value: string) => {
      if (this.iterationStatus && $this.callBack(value)) {
        const currentTimeout = Math.min($this.timeout * 1.3, $this.maxTimeout);
        $this.timeout = currentTimeout;
        setTimeout(() => this.asyncTimeOutFn($this), currentTimeout);
      }
    });
  }
  asyncTimeOutSignFn($this: asyncCallParamsAuditT) {
    $this.service().then(async (value: string) => {
      $this.callBack(value).then((e) => {
        if (e) {
          const currentTimeout = Math.min(
            $this.timeout * 1.3,
            $this.maxTimeout
          );
          $this.timeout = currentTimeout;
          setTimeout(() => this.asyncTimeOutSignFn($this), currentTimeout);
        }
      });
    });
  }
  getPDF({ id }: { id: string | number }) {
    // return id of consulting
    return new Promise(async (resolve, reject) => {
      this.http
        .get(
          environment.apigateway_url +
            '/generatePdf?idOferta=' +
            id +
            '?code=' +
            (await this.authService.currentAuthToken),
            {
              headers:{
                'Cache-Control': 'no-store, no-cache, must-revalidate',
                
              }
            }
        )
        .subscribe((value) => {
          resolve(value);
        });
    });
  }
  getFirmantes({ rut }: { rut: string }): Promise<FirmantesDto | null> {
    // return id of consulting
    return new Promise(async (resolve, reject) => {
      this.http
        .post<FirmantesDto>(
          environment.apigateway_url + '/sherlock-integration/firmante',
          {
            rutSociedad: rut,
          },
          {
            headers: {
              'Content-Type': 'application/json',
              'Cache-Control': 'no-store, no-cache, must-revalidate',
              
              Authorization:
                'Bearer ' + (await this.authService.currentAuthToken),
            },
            context: new HttpContext().set(ALERT_FLAG, true),
          }
        )
        .subscribe(
          (value) => {
            resolve(value);
          },
          (error) => {
            // Handle error
            console.error('An error occurred ON FIRMANTES:', error);
            reject(error);
          }
        );
    });
  }
  updateOfferSelectedInfo({ id, data }: { id: string | number; data: any }) {
    // return id of consulting
    return new Promise(async (resolve, reject) => {
      this.http
        .patch<any>(
          environment.apigateway_url + '/offers/' + id + '/selection',
          data,
          {
            headers: {
              'Content-Type': 'application/json',
              'Cache-Control': 'no-store, no-cache, must-revalidate',
              
              Authorization:
                'Bearer ' + (await this.authService.currentAuthToken),
            },
          }
        )
        .subscribe((value) => {
          resolve(value);
        });
    });
  }

  // FORM SERVICES
  // post service
  postNewConsulting(form: ConsultaOfertaT): Promise<{ id: string }> {
    return new Promise(async (resolve, reject) => {
      this.http
        .post<{
          offerId: string;
        }>(environment.apigateway_url + '/offers', form, {
          headers: {
            'Content-Type': 'application/json',
            'Cache-Control': 'no-store, no-cache, must-revalidate',
            
            Authorization:
              'Bearer ' + (await this.authService.currentAuthToken),
          },
        })
        .subscribe((e) => resolve({ id: e.offerId }));
    });
  }
  getInmuebles(): Promise<TipoInmuebleDto[] | null> {
    // eslint-disable-next-line no-async-promise-executor
    return new Promise(async (resolve, reject) => {
      const cache = getCache('getInmuebles');
      if (cache) {
        resolve(cache);
      } else {
        this.http
          .get<TipoInmuebleDto[]>(
            environment.apigateway_url + '/tipo-inmueble',
            {
              headers: {
                'Content-Type': 'application/json',
                'Cache-Control': 'no-store, no-cache, must-revalidate',
                
                Authorization:
                  'Bearer ' + (await this.authService.currentAuthToken),
              },
            }
          )
          .subscribe((value) => {
            createCache('getInmuebles', value, 0);
            resolve(value);
          });
      }
    });
  }

  getSegmentos(): Promise<TipoClienteDto[] | null> {
    // return id of consulting
    return new Promise(async (resolve, reject) => {
      const cache = getCache('getSegmentos');
      if (cache) {
        resolve(cache);
      } else {
        this.http
          .get<TipoClienteDto[]>(environment.apigateway_url + '/tipo-cliente', {
            headers: {
              'Content-Type': 'application/json',
              'Cache-Control': 'no-store, no-cache, must-revalidate',
              
              Authorization:
                'Bearer ' + (await this.authService.currentAuthToken),
            },
          })
          .subscribe((value) => {
            createCache('getSegmentos', value, 0);
            resolve(value);
          });
      }
    });
  }
  getRecursos(): Promise<TipoRecursoDto[] | null> {
    // return id of consulting
    return new Promise(async (resolve, reject) => {
      const cache = getCache('getRecursos');
      if (cache) {
        resolve(cache);
      } else {
        this.http
          .get<TipoRecursoDto[]>(environment.apigateway_url + '/tipo-recurso', {
            headers: {
              'Content-Type': 'application/json',
              'Cache-Control': 'no-store, no-cache, must-revalidate',
              
              Authorization:
                'Bearer ' + (await this.authService.currentAuthToken),
            },
          })
          .subscribe((value) => {
            createCache('getRecursos', value, 0);
            resolve(value);
          });
      }
    });
  }
  /* ---------------------------------------------------- */
  getHistory(queryParams?: string): any | Promise<any> {
    const urlHistoryFull = queryParams ? '/offers?' + queryParams : '/offers';

    return new Promise(async (resolve, reject) => {
      this.http
        .get<TipoHistorialDto[]>(environment.apigateway_url + urlHistoryFull, {
          headers: {
            'Content-Type': 'application/json',
            'Cache-Control': 'no-store, no-cache, must-revalidate',
            
            Authorization:
              'Bearer ' + (await this.authService.currentAuthToken),
          },
        })
        .subscribe((value) => {
          resolve(value);
        });
    });
  }
  /* ---------------------------------------------------- */
}

const formatOfferData = (value: offerBaseT) => {
  const data = value;
  const productosArray: productListT[] = [];
  _.each(data.productos, (producto) => {
    const detalleOfertaReduce = _.reduce(
      producto.detalleOferta,
      function (curr, next, index) {
        if (index === 0) return curr;
        const indexAlreadyOnNewSet = _.findIndex(
          curr,
          (detailProduct) =>
            detailProduct.idDetalleOferta === next.idDetalleOferta
        );
        if (indexAlreadyOnNewSet !== -1) {
          curr[indexAlreadyOnNewSet].medioPagoMonitoreo.push(
            next.medioPagoMonitoreo
          );
          curr[indexAlreadyOnNewSet] = {
            idDetalleOferta: next.idDetalleOferta,

            montoInstalacion: next.montoInstalacion,
            monedaInstalacion: next.monedaInstalacion,
            montoServicio: next.montoServicio,
            monedaServicio: next.monedaServicio,
            glosaNodo: next.glosaNodo,
            vigencia: next.vigencia,
            medioPagoMonitoreo: curr[indexAlreadyOnNewSet].medioPagoMonitoreo,
            autorizacionId: next.autorizacionId,
            autorizacion: next.autorizacion,
            medioPagoInstalacion: next.medioPagoInstalacion,
            datosAdicionales: {
              ...next.datosAdicionales,
              tipoCuotaGlosa:
                (next.categoria ? next.categoria + ' , ' : '') +
                next.datosAdicionales.tipoCuotaGlosa,
            },
          };
        } else {
          curr.push({
            idDetalleOferta: next.idDetalleOferta,
            montoInstalacion: next.montoInstalacion,
            monedaInstalacion: next.monedaInstalacion,
            montoServicio: next.montoServicio,
            monedaServicio: next.monedaServicio,
            vigencia: next.vigencia,
            glosaNodo: next.glosaNodo,
            medioPagoMonitoreo: [next.medioPagoMonitoreo],
            autorizacionId: next.autorizacionId,
            autorizacion: next.autorizacion,
            medioPagoInstalacion: next.medioPagoInstalacion,
            datosAdicionales: next.datosAdicionales,
          });
        }

        return curr;
      },
      [
        {
          idDetalleOferta: producto.detalleOferta[0].idDetalleOferta,
          montoInstalacion: producto.detalleOferta[0].montoInstalacion,
          monedaInstalacion: producto.detalleOferta[0].monedaInstalacion,
          montoServicio: producto.detalleOferta[0].montoServicio,
          monedaServicio: producto.detalleOferta[0].monedaServicio,
          vigencia: producto.detalleOferta[0].vigencia,
          medioPagoMonitoreo: [producto.detalleOferta[0].medioPagoMonitoreo],
          autorizacionId: producto.detalleOferta[0].autorizacionId,
          glosaNodo: producto.detalleOferta[0].glosaNodo,
          autorizacion: producto.detalleOferta[0].autorizacion,
          medioPagoInstalacion: producto.detalleOferta[0].medioPagoInstalacion,

          datosAdicionales: producto.detalleOferta[0].datosAdicionales,
        },
      ]
    );
    productosArray.push({
      ...producto,
      paycodeList: detalleOfertaReduce
        .flatMap((obj) => obj.medioPagoMonitoreo)
        .filter((valor, index, array) => array.indexOf(valor) === index)
        .map((valor) => ({ value: valor, label: valor })),
      detalleOferta: detalleOfertaReduce,
    });
  });

  return {
    ...data,
    productos: productosArray,
    modo: data.modo ?? 1,
    estado: data.estado ?? offerResponseStatus.ERROR,
  };
};

